var Components = require('../../components.json');
var fs = require('fs');
var render = require('json-templater/string');
var uppercamelcase = require('uppercamelcase');
var path = require('path');
var endOfLine = require('os').EOL;

var OUTPUT_PATH = path.join(__dirname, '../../src/examples.js');
var NAME_TEMPLATE = '../../src/lib/{{component}}/examples/index.json';
var IMPORT_TEMPLATE = 'import {{name}} from \'./lib/{{component}}/examples/{{file}}\';';
var INSTALL_COMPONENT_TEMPLATE = '  {{name}}';
var MAIN_TEMPLATE = `/* Generated by './build/bin/build-example.js' */

{{include}}

const components = [
{{install}}
];

const install = function(Vue) {
  components.forEach(component => {
    Vue.component(component.name, component);
  });
};

if (typeof window !== 'undefined' && window.Vue) {
  install(window.Vue);
}

export default {
  install,
{{list}}
};
`;

var ComponentNames = Object.keys(Components);

var includeComponentTemplate = [];
var installTemplate = [];
var listTemplate = [];

ComponentNames.forEach(name => {
  var ExampleComponentNames = require(render(NAME_TEMPLATE, {
    component: name
  }));

  ExampleComponentNames.forEach(exampleName => {
    var componentName = uppercamelcase(exampleName);

    includeComponentTemplate.push(render(IMPORT_TEMPLATE, {
      name: componentName,
      component: name,
      file: exampleName
    }));

    installTemplate.push(render(INSTALL_COMPONENT_TEMPLATE, {
      name: componentName,
      component: exampleName
    }));

    listTemplate.push(`  ${componentName}`);
  });
});

var template = render(MAIN_TEMPLATE, {
  include: includeComponentTemplate.join(endOfLine),
  install: installTemplate.join(',' + endOfLine),
  list: listTemplate.join(',' + endOfLine)
});

fs.writeFileSync(OUTPUT_PATH, template);
console.log('[build example] DONE:', OUTPUT_PATH);
